import Foundation
import UIKit

class Utils {
    
    static func getScreenHeight() -> CGFloat {
        return UIScreen.main.bounds.size.height
    }
    
    static func getScreenWidth() -> CGFloat {
        return UIScreen.main.bounds.size.width
    }
    
}
